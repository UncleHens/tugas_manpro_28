            <!-- Bab 1 -->
            <div class="chapter">
                <h2>1. Awal Mula (200 SM)</h2>
                <h3>Salju, Susu, dan Kaisar</h3>
                <p>Sejarah es krim dimulai jauh sebelum lemari pendingin ditemukan. Di <strong>Cina kuno</strong> sekitar tahun 200 SM, orang-orang menciptakan hidangan dari susu kerbau, tepung, dan kapur barus yang dibekukan menggunakan salju yang dicampur dengan garam sendawa.</p>
                
                <div class="image-container">
                    <!-- Gambar ilustrasi salju/pegunungan -->
                    <img src="https://images.unsplash.com/photo-1517299321609-52687d1bc55a?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&q=80" alt="Salju Pegunungan">
                </div>

                <p>Sementara itu di Romawi, <strong>Kaisar Nero</strong> (37-68 M) dikenal mengirim pelari cepat ke pegunungan untuk mengambil salju. Salju tersebut kemudian dicampur dengan madu, buah-buahan, dan nektar bunga. Ini lebih mirip dengan "sorbet" atau es serut modern daripada es krim susu yang kita kenal sekarang.</p>
            </div>

            <!-- Bab 2 -->
            <div class="chapter">
                <h2>2. Perjalanan ke Eropa</h2>
                <h3>Oleh-oleh Marco Polo</h3>
                <p>Selama berabad-abad, resep es krim menghilang atau menjadi rahasia lokal. Titik baliknya terjadi pada abad ke-13. Penjelajah terkenal, <strong>Marco Polo</strong>, kembali ke Italia dari perjalanannya ke Timur Jauh (Cina) dengan membawa resep yang menyerupai sorbet susu.</p>
                
                <div class="image-container">
                    <!-- Gambar ilustrasi nuansa eropa klasik/venezia -->
                    <img src="https://images.unsplash.com/photo-1520116468816-95b69f847357?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&q=80" alt="Suasana Eropa Klasik">
                </div>

                <p>Resep ini kemudian berkembang di Italia. Ketika Catherine de' Medici dari Italia menikah dengan Raja Henry II dari Prancis pada tahun 1533, dia membawa serta para koki pembuat es krimnya. Sejak saat itu, hidangan ini menjadi makanan penutup elit bagi kaum bangsawan Prancis.</p>
            </div>

            <!-- Bab 3 -->
            <div class="chapter">
                <h2>3. Es Krim untuk Rakyat</h2>
                <h3>Kafe Procope & Amerika</h3>
                <p>Hingga tahun 1600-an, es krim adalah hidangan eksklusif kerajaan. Namun pada akhir abad ke-17, <strong>CafÃ© Procope</strong> di Paris mulai menyajikan es krim (yang dicampur susu, krim, mentega, dan telur) kepada masyarakat umum, termasuk tokoh-tokoh seperti Napoleon dan Victor Hugo.</p>
                
                <p>Resep ini kemudian menyeberangi lautan menuju Amerika. Presiden George Washington dan Thomas Jefferson dikenal sangat menyukai es krim. Bahkan, Jefferson memiliki resep es krim vanilla tulisan tangannya sendiri!</p>
                
                <div class="fun-fact">
                    <strong>Fakta Unik:</strong> Presiden George Washington dikabarkan menghabiskan sekitar $200 (setara ribuan dolar nilai sekarang) untuk es krim selama musim panas tahun 1790.
                </div>
            </div>

            <!-- Bab 4 -->
            <div class="chapter">
                <h2>4. Revolusi Industri</h2>
                <h3>Mesin Pemutar Tangan</h3>
                <p>Sebelum tahun 1800-an, membuat es krim sangat sulit dan memakan waktu. Semuanya berubah pada tahun 1843 ketika <strong>Nancy Johnson</strong> dari Amerika mematenkan "pembuat es krim engkol tangan" (hand-cranked ice cream maker).</p>
                
                <div class="image-container">
                    <!-- Gambar tekstur es krim -->
                    <img src="https://images.unsplash.com/photo-1557142046-c704a3adf364?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&q=80" alt="Tekstur Es Krim">
                </div>

                <p>Alat ini menggunakan garam dan es di wadah luar untuk membekukan krim di wadah dalam sambil diaduk dengan engkol. Inovasi ini membuat tekstur es krim menjadi lebih halus dan proses pembuatannya jauh lebih cepat. Tak lama kemudian, Jacob Fussell membangun pabrik es krim industri pertama, membuat harga es krim menjadi terjangkau bagi semua orang.</p>
            </div>

            <!-- Bab 5 -->
            <div class="chapter">
                <h2>5. Penemuan Cone</h2>
                <h3>Pameran Dunia 1904</h3>
                <p>Bagaimana dengan cone (corong) es krim? Penemuan ini terjadi secara tidak sengaja pada <strong>World's Fair 1904 di St. Louis</strong>. Seorang penjual es krim kehabisan mangkuk kertas.</p>
                
                <div class="image-container">
                    <!-- Gambar Cone Es Krim -->
                    <img src="https://images.unsplash.com/photo-1501443762994-82bd5dace89a?ixlib=rb-4.0.3&auto=format&fit=crop&w=800&q=80" alt="Ice Cream Cone">
                </div>

                <p>Di sebelahnya, seorang penjual wafel Suriah bernama Ernest Hamwi sedang sepi pembeli. Hamwi kemudian menggulung wafelnya menjadi bentuk kerucut agar bisa menampung es krim. Kolaborasi dadakan ini menciptakan "World's Fair Cornucopia" yang kini kita kenal sebagai <em>ice cream cone</em>.</p>
            </div>

            <!-- Bab 6 -->
            <div class="chapter">
                <h2>6. Era Modern</h2>
                <h3>Varian Tanpa Batas</h3>
                <p>Dengan ditemukannya lemari pendingin listrik (kulkas) yang murah di pertengahan abad ke-20, es krim masuk ke dalam rumah tangga di seluruh dunia. Muncul berbagai variasi baru: <em>Soft serve</em> (seperti di restoran cepat saji), Gelato (versi Italia yang lebih padat), hingga es krim nitrogen cair.</p>
                
                <p>Hari ini, es krim bukan lagi sekadar susu dan gula. Kita bisa menemukan rasa yang unik mulai dari matcha, arang (charcoal), hingga rasa rendang atau kecap manis!</p>
                
