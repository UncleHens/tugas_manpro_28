                <p>Siapa yang bisa menolak satu skop es krim di hari yang panas? Dessert beku yang lembut dan manis ini adalah salah satu makanan paling populer di dunia. Namun, sebelum kita bisa menikmatinya dari freezer mini market, es krim telah melalui perjalanan sejarah yang sangat panjang, melibatkan kaisar, penjelajah, hingga inovasi teknologi.</p>
